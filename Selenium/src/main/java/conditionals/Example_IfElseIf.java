package conditionals;

public class Example_IfElseIf {

	public static void main(String[] args) {
		int i = 25;
		
		if (i == 10) {
			System.out.print("i is equal to 10");
		}else if (i == 15){
			System.out.print("i is equal to 15");
		}else if (i == 20) {
			System.out.print("i is equal to 20");
		}else {
			System.out.print("i is neither none of the options");
		}

	}

}
